import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error, r2_score
import matplotlib.pyplot as plt
import numpy as np

dataset=pd.read_csv('/content/expanded_foodcompany.csv')

dataset.head()

X = dataset[["Weather_Condition", "Day_of_Week", "Special_Day", "Ad_Spend"]]
y = dataset["Visitor_Count"]

print(X)

print(y)

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.3, random_state=0)

model = LinearRegression()
model.fit(X_train, y_train)

print("Model coefficients:", model.coef_)
print("Model intercept:", model.intercept_)

new_data = pd.DataFrame([[26, 2, 0, 450]], columns=["Weather_Condition", "Day_of_Week", "Special_Day", "Ad_Spend"])

y_pred_new = model.predict(new_data)
print(y_pred_new)

y_pred = model.predict(X_test)

rmse = np.sqrt(mean_squared_error(y_test, y_pred))
print("Root Mean Squared Error (RMSE):", rmse)

# Grafik
plt.figure(figsize=(10, 6))
plt.scatter(y_test, y_pred, color='blue', label='Actual')
plt.plot([y_test.min(), y_test.max()], [y_test.min(), y_test.max()], 'r--', label='Perfect Prediction')
plt.title('Actual vs. Predicted Visitor Count')
plt.xlabel('Actual Visitor_Count')
plt.ylabel('Predicted Visitor_Count')
plt.legend()
plt.grid(True)
plt.show()